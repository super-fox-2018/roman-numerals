function to_roman (num) {
  const romans = { 1: 'I', 4: 'IV', 5: 'V', 9: 'IX', 10: 'X', 40: 'XL', 50: 'L', 90: 'XC', 100: 'C', 400: 'CD', 500: 'D', 900: 'CM', 1000: 'M'};
  let roman = '';
  const numbers = Object.keys(romans);
  let i = numbers.length - 1;
  while (num !== 0) {
    if (num - numbers[i] >= 0) {
      num -= numbers[i];
      roman += romans[numbers[i]];
    } else i -= 1;
  }

  return roman;
}

